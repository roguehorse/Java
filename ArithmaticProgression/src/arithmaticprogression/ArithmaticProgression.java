package arithmaticprogression;

/**
 *
 * @author Roguehorse
 */
public class ArithmaticProgression 
{

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) 
    {
        int sum = 0;
        int i = 1;

     while (i <= 1000) i++;
      {
         sum = sum + i;
         System.out.println(Integer.toString(sum));
      } 
    }
}
  

